//equal to 
function equal(a){
    console.log (a === 5);
}
equal(5) //true


//lessthan and greaterthan 

function greaterthan(a,b){
    console.log( a > b);
}
greaterthan(4,6); //false

function lessthan(a,b){
    console.log(a < b);
}
lessthan(7,5);//true


//Math.min() and Math.max()

function min(a,b){
    console.log(Math.min(a,b));
}
min(3,5); //3

function max(a,b){
    console.log(Math.max(a,b));
}
max(8,6);//8




