
//addition
function add(a, b) {
    return a + b;
  }
  console.log(add(3,4));

  //subtraction
  function sub(a,b){
    return a - b;
  }
  console.log(sub(5,2));
  
  //multiply
  function multiply(a,b){
    return a * b;
  }
  console.log(multiply(6,3));

  //divided
  function divided(a,b){
    return a / b;
  }
  console.log(divided(15,3));
  
  //modulus
  function modulus(a,b){
    return a%b;
  }
  console.log(modulus(10, 3));  

